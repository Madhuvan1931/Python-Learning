# Arithmetic operators: +, -, *, / etc. 
a = 4
b = 8
c = a+b
print("Arithmetic Operator")
print(c)

# Assignment operators:  =, +=, -= etc. 

a = 2-4 # assign 2-4 in a
b = 6
# b += 3 # Increment the value of b by 3 and then assign it to b
b -= 3 # Decrement the value of b by 3 and then assign it to b
print("Assignment Operator")
print(b)

# Comparison operators: ==, >, >=, <,  != etc. 
# return always boolean value

print("Comparison Operator")
print(4==4)
print(4<6)
print(4>6)
print(5!=7)

# Logical operators: and, or, not. 

e = True or False

print("Logical Operator")
print(e)
print("Truth Table: (OR)- ")
print("True or False is ",True or False)
print("True or True is ",True or True)
print("False or True is ",False or True)
print("False or False is ",False or False)

print("Truth Table: (AND)- ")
print("True and False is ",True and False)
print("True and True is ",True and True)
print("False and True is ",False and True)
print("False and False is ",False and False)

# for 'not'

print(not(True))
print(not(False))